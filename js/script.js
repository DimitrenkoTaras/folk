$(document).ready(function() {
    $('.header__burger').click(function(event) {
        $('.header__burger, .header__menu').toggleClass('active');
        $('body').toggleClass('lock');
    });
    $('.header__list').click(function(event) {
        $('.header__burger, .header__menu').removeClass('active');
        $('body').removeClass('lock');
    });

    $('.skills-slider').slick({
        speed:1500,   
    });

    $('.portfolio__item.f_4, .portfolio__item.f_5').hide();
    $('.portfolio__btn').click(function(event) {
        $('.portfolio__item.f_4, .portfolio__item.f_5').show();
        $('.portfolio__btn').hide();
    });

    $('.filter__item').click(function(event) {
        var i=$(this).data('filter');
    if (i==1) {
        $('.portfolio__item.f_2, .portfolio__item.f_3').show();
        $('.portfolio__item.f_4, .portfolio__item.f_5').hide();
        $('.portfolio__btn').show();
    } else {
        $('.portfolio__item').hide();
        $('.portfolio__item.f_'+i).show();
        $('.portfolio__btn').hide();
    }
    $('.filter__item').removeClass('active');
    $(this).addClass('active')

    return false;
    });
});

